    val advice: String
) {
    fun getIntroByScore(score: Float): String {
        return when {
            score == 0F -> {
                intro[0]
            }
            score <= 30f -> {
                intro[1]
            }
            score <= 60f -> {
                intro[2]
            }
            else -> {
                intro[3]
            }
        }
    }
}
