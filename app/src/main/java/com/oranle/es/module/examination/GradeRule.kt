    /**
     *  多元智能测评表评分规则
     *  总分：90
     *  题目数：15
     *  每题满分：6分
     *
     *  选择项 得分
     *   A	   0
     *   B	   2
     *   C	   4
     *   D	   6
     */
    @JvmStatic
    fun gradeScore(bean: SingleChoiceWrap) = if (bean.isMultiSmartSheet) {
        when (bean.selectOption) {
            "A" -> 0F
            "B" -> 2F
            "C" -> 4F
            "D" -> 6F
            else -> 0F
        }
    } else {
        if (bean.rightAnswer == bean.selectOption!!)
            bean.rule.calculatedScore
        else
            0f
    }

    fun getMultiSheet(typeStr: String, score: Float): MultiSmartSheet? {
        multiSheetData.forEach {
            if (it.title == typeStr) {
                return it
            }
        }
        return null
    }

